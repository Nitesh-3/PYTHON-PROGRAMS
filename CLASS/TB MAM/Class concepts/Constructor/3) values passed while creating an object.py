class rectangle:
    def __init__(self,l,b):
        self.length=l
        self.breadth=b
        print(f'The length is {self.length} and t breadth is {self.breadth}')

    def area():
        self.area=self.length*self.breadth
        print('The area is ',self.area )

    def perimeter():
        self.perimeter=2(self.length*self.breadth)
        print('The perimeter is ',self.perimeter)

r1=rectangle(20,15)
r1.area()
r1.perimeter()
